'''
In the code below, we have a dictionary called student. 
Inside this dictionary, we have three things: 'name', 'age', and 'course'. 
'name' is a key that points to the student's name, which is 'Eric'. 
'age' is another key, and it tells us the student's age, which is 36.  
'course' is a key for a list of things the student is studying, which are 'Math' and 'Computer Science'.

When we write print(student), it's like asking Python to show us everything that's inside the student dictionary.
Python will print out the whole dictionary, including all the keys and their associated information.
'''

student = {'name': 'Eric', 'age': 36, 'course': ['Math', 'Computer Science']}
print(student)